def crunch(str)
  results = [str[0]]
  prev_char = str[0]
  str.each_char do |char|
    next if char == prev_char
    results << char
    prev_char = char
  end
  results.join
end

p crunch('ddaaiillyy ddoouubbllee') == 'daily double'
p crunch('4444abcabccba') == '4abcabcba'
p crunch('ggggggggggggggg') == 'g'
p crunch('a') == 'a'
p crunch('') == ''

